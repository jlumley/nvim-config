return {
  {
    'akinsho/toggleterm.nvim',
    version = '*',
    opts = {
      direction = 'float',
    },
  },
  vim.keymap.set('n', '<leader>i', ':ToggleTerm direction=float<CR>', { noremap = true, silent = true, desc = 'Open [I]terminal window' }),
  vim.keymap.set('t', '<leader>i', [[<C-\><C-n>:ToggleTerm<CR>]], { noremap = true, silent = true, desc = 'Minimize [I]Terminal window' }),
}
